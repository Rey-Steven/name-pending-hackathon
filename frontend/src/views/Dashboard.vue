<script setup lang="ts">
import { onMounted, onUnmounted, ref, watch } from 'vue'
import { useDashboardStore } from '../stores/dashboard'
import { workflowApi } from '../api/client'
import AgentPipeline from '../components/AgentPipeline.vue'
import PendingOffers from '../components/PendingOffers.vue'

const store = useDashboardStore()
const pendingOffersRef = ref<InstanceType<typeof PendingOffers> | null>(null)

const pipelineStages = [
  { key: 'lead_contacted', name: 'Lead Contacted', bg: 'bg-blue-50', text: 'text-blue-700', label: 'text-blue-500' },
  { key: 'in_pipeline',   name: 'In Pipeline',    bg: 'bg-indigo-50', text: 'text-indigo-700', label: 'text-indigo-500' },
  { key: 'offer_sent',    name: 'Offer Sent',     bg: 'bg-purple-50', text: 'text-purple-700', label: 'text-purple-500' },
]

function formatCurrency(value: number) {
  if (!value) return '‚Ç¨0'
  return '‚Ç¨' + value.toLocaleString('el-GR', { minimumFractionDigits: 0, maximumFractionDigits: 0 })
}

const agentLabels: Record<string, string> = {
  marketing: 'üéØ Marketing',
  sales: 'üíº Sales',
  legal: '‚öñÔ∏è Legal',
  accounting: 'üìä Accounting',
  email: 'üìß Email',
}

function getAgentLabel(agent: string) {
  return agentLabels[agent] || agent
}

function getEventClass(event: any) {
  const base: Record<string, string> = {
    marketing: 'border-marketing bg-marketing-light',
    sales: 'border-sales bg-sales-light',
    legal: 'border-legal bg-legal-light',
    accounting: 'border-accounting bg-accounting-light',
    email: 'border-email bg-email-light',
  }
  return base[event.agent] || 'border-gray-200'
}

function formatTime(ts: string) {
  return new Date(ts).toLocaleTimeString()
}

async function startWorkflow(leadId: string) {
  store.setWorkflowRunning(leadId)
  try {
    await workflowApi.start(leadId)
  } catch (e: any) {
    console.error('Workflow error:', e)
    store.isWorkflowRunning = false
  }
}

// Reload pending offers panel whenever a new offer draft SSE event arrives
watch(() => store.agentEvents[0], (event) => {
  if (event?.type === 'offer_pending_approval') {
    pendingOffersRef.value?.load()
  }
})

onMounted(() => {
  store.refreshAll()
  store.connectSSE()
})

onUnmounted(() => {
  store.disconnectSSE()
})
</script>
